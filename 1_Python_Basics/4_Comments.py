# This is a single-line comment
# Comments code को explain करने के लिए use होते हैं
# Python interpreter comments को ignore करता है

a = 4   # variable 'a' को value 4 assign की गई
b = 6   # variable 'b' को value 6 assign की गई

# Second comment explaining the operation
print(a + b)   # a और b का sum print करेगा