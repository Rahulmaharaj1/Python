# Identifier rules in Python

# 1. Identifier digit से start नहीं हो सकता
name1 = "Nitish"
print(name1)

# 2. Identifier में underscore (_) use कर सकते हैं
_name = "Nitish Kumar"
print(_name)

# 3. Identifier Python keyword नहीं हो सकता
# Example (गलत):
# class = "Python"   # ❌ Error देगा क्योंकि 'class' keyword है

# 4. Identifier letters, digits और underscore से बन सकता है
student_age = 20
print(student_age)