# -------------------------------
# Python Data Types Examples
# -------------------------------

# Integer
print(8)  
# Integer whole number होता है

# Very large number (Scientific notation)
print(1e309)  
# e का मतलब 10 power होता है
# 1e309 = 1 × 10^309 (बहुत बड़ा number)

# -------------------------------
# Decimal / Float
# -------------------------------

print(8.55)  
# Float decimal number होता है

print(1.7e309)  
# Scientific notation with float

# -------------------------------
# Boolean
# -------------------------------

print(True)  
print(False)  
# Boolean केवल दो values होती हैं: True या False

# -------------------------------
# Text / String
# -------------------------------

print('Hello World')  
# String text data को represent करता है

# -------------------------------
# Complex Number
# -------------------------------

print(5 + 6j)  
# Complex number = real part + imaginary part
# j imaginary unit होता है

# -------------------------------
# List (similar to array in C)
# -------------------------------

print([1,2,3,4,5])  
# List ordered collection होती है
# List change (mutable) की जा सकती है

# -------------------------------
# Tuple
# -------------------------------

print((1,2,3,4,5))  
# Tuple list की तरह होता है लेकिन immutable (change नहीं कर सकते)

# -------------------------------
# Set
# -------------------------------

print({1,2,3,4,5})  
# Set unordered collection होता है
# Duplicate values allow नहीं करता

# -------------------------------
# Dictionary
# -------------------------------

print({'name':'Nitish','gender':'Male','weight':70})  
# Dictionary key-value pair store करता है

# -------------------------------
# Type Function
# -------------------------------

print(type([1,2,3]))  
# type() function किसी data का datatype बताता है